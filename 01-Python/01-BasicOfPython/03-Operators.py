# Operatprs...
num1 = int(input("Enter the first number : "))
num2 = int(input("Enter your second number : "))

# Airthmetic Operators : + , - , * , / , % , ** , //
print("Airthmetic Operations are :")
print(f"The sum of {num1} and {num2} is : {num1 + num2}")
print(f"The difference of {num1} and {num2} is : {num1 - num2}")
print(f"The product of {num1} and {num2} is : {num1 * num2}")
print(f"The quotient of {num1} and {num2} is : {num1 / num2}")
print(f"The modulus of {num1} and {num2} is : {num1 % num2}")
print(f"The power of {num1} and {num2} is : {num1 ** num2}")
print(f"The floor division of {num1} and {num2} is : {num1 // num2}")

# Comparison Operator : == , != , < , > , <= , >= 
print("Comparison Operations are :")
print(f"{num1} == {num2} = {num1 == num2}")
print(f"{num1} != {num2} = {num1 != num2}")
print(f"{num1} < {num2} = {num1 < num2}")
print(f"{num1} > {num2} = {num1 > num2}")
print(f"{num1} <= {num2} = {num1 <= num2}")
print(f"{num1} >= {num2} = {num1 >= num2}")

# Logical Operators : and , or , not
print("Logical Operations are :")
print(f"{num1} > 10 and {num2} > 10 = {num1 > 10 and num2 > 10}")
print(f"{num1} > 10 or {num2} > 10 = {num1 > 10 or num2 > 10}")
print(f"not({num1} > 10) = {not(num1 >  10)}")